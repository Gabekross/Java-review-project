package com.hr.personnel;

import static org.junit.Assert.*;

import java.time.LocalDate;
import org.assertj.core.api.Assertions;
import org.junit.Assert;
import org.junit.Before;
import org.junit.Test;

public class EmployeeTest {

  private Employee employee;

  @Before
  public void setUp() throws Exception{

    Employee employee = new Employee("olu", LocalDate.of(2020, 3, 03));


  }

  @Test
  public void getEmployeeInfo_should_return_name_and_hireDate() {


    String employeeInfo = employee.getEmployeeInfo();

    //assert
    Assert.assertEquals("name = olu, hireDate = 2020-01-03", employeeInfo);

    Assertions.assertThat(employeeInfo).isEqualTo("olu, hireDate = 2020-01-03");
  }

  @Test
  public void work() {
    String work = employee.work();
    Assertions.assertThat(work).isEqualTo("olu worked");
  }
}